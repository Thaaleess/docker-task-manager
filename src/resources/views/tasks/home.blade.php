    <h3>⚙️ Gerenciador de Tarefas Simples ⚙️</h3>
    <hr>
    <p>
    Este projeto foi desenvolvido com o objetivo de oferecer uma forma prática e intuitiva de organizar qualquer tipo de tarefas.
    O sistema possibilita a criação de tarefas, marcar elas como concluídas e excluí-las e editá-las, caso necessário.</p>
    <hr>
    <p>O sistema utiliza requisições AJAX para atualizar as informações em tempo real.
    Além disso, as tarefas são exibidas com paginação dinâmica e ordenadas automaticamente pela data de vencimento mais próxima, ajudando a manter o foco no que é mais urgente.</p>
    <hr>
    <p>💻 Tecnologias utilizadas:</p>
    <p>● Docker & Docker Compose — Containerização de todo o ambiente (PHP, MySQL, Node e phpMyAdmin).</p>
    <p>● Laravel (PHP) — Responsável pelo back-end, controle de rotas e lógica do sistema.</p>
    <p>● Vite — Build de assets do Laravel.</p>
    <p>● JavaScript (AJAX) — Usado para atualizar o conteúdo de forma assíncrona.</p>
    <p>● Bootstrap + Bootstrap Icons — Para modais, layout responsivo, estilização da interface e ícones personalizados.</p>
    <p>● MySQL — Armazenamento das tarefas e controle de dados.</p>
    <p>● Node.js + npm — Usado via container para gerenciar pacotes e rodar o Vite.</p>
</x-layout>